            <h5>Aproveitamento de Estudos </h5>
            <div class="divider"></div>
            <br>

            Nesta página encontram-se as perguntas mais frequentes referentes ao processo de aproveitamento de estudos e como realizá-lo.

            <br><br>

            <ul class="collapsible">
                <li>
                    <div class="collapsible-header"><b>1 - &nbsp;</b>O que é o Aproveitamento de Estudos?</div>
                    <div class="collapsible-body"><span>
                            É o  aproveitamento de estudos de componentes curriculares, previsto
                            em calendário acadêmico, mediante requerimento realizado por esse sistema.
                            Os currículos poderão ter sido cursados em diferentes instituições credenciadas pelos sistemas federal, estadual e municipal de ensino.
                        </span></div>
                </li>
                
                <li>
                    <div class="collapsible-header"><b>2 - &nbsp;</b>Quais documentos eu devo enviar?</div>
                    <div class="collapsible-body"><span>


                            <ul>
                                <li> I – Histórico acadêmico;</li>
                                <li>II – Matriz curricular cursada;</li>
                                <li>III – Planos de ensino dos componentes curriculares com especificação de carga horária
                                    comprovada e conteúdo programático.</li>
                            </ul>
                            <br>
                            Lembre-se de especificar qual documento está sendo enviado utilizando o campo "descrição" na página de envio de arquivos para auxiliar o seu professor indicando
                            qual documento da sua instituição de origem está sendo enviado.

                        </span></div>
                </li>
                
                <li>
                    <div class="collapsible-header"><b>3 - &nbsp;</b>Qual formato é aceito? Posso colocar todos os documentos em um único arquivo?</div>
                    <div class="collapsible-body"><span>

                            O único formato aceito é PDF(Portable Document Format),sendo no máximo 5mb por arquivo e até 4 arquivos por solicitação. A inclusão dos documentos necessários(pergunta nº 2), bem como sua divisão é de preferência do estudante.
                            Entretanto, recomenda-se que os documentos possuam as devidas descrições para um maior entendimento por parte do avaliador.
                         
                        </span></div>
                </li>
                
                <li>
                    <div class="collapsible-header"><b>4 - &nbsp;</b>Qual o critério utilizado para equivalência dos componentes curriculares cursados e do IFB?</div>
                    <div class="collapsible-body"><span>

                            Será considerada uma equivalência mínima de 75% tanto na carga horária quanto

                            nos conteúdos entre os componentes curriculares cursados e os do curso no IFB.
                         
                        </span></div>
                </li>
                
                <li>
                    <div class="collapsible-header"><b>5 - &nbsp;</b>Posso alterar minha solicitação após enviada?</div>
                    <div class="collapsible-body"><span>
                            Sim. Clique em acompanhar, escolha o curso e clique na opção "Refazer". O aluno só pode refazer uma solicitação dentro do período de inscrições.
                            
                         
                        </span></div>
                </li>
                
                 <li>
                    <div class="collapsible-header"><b>6 - &nbsp;</b>Tive minha solicitação indeferida, posso realizar nova solicitação?</div>
                    <div class="collapsible-body"><span>
                            Sim, basta esperar o processo seguinte que ocorre semestralmente.
                         
                        </span></div>
                </li>
                
               

            </ul>


        </div>


    </div>

